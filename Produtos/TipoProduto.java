package Produtos;

/**
 * TipoProduto representa os diferentes tipos de produtos existentes.
 * Utilizada para identificar o tipo de produto.
 */
public enum TipoProduto {
    ROUPA, // Produto do tipo roupa
    ACESSORIO, // Produto do tipo acessório
    BRINQUEDO_PEQUENO, // Produto do tipo brinquedo pequeno
    BRINQUEDO_GRANDE, // Produto do tipo brinquedo grande
    ELETRONICO_PEQUENO, // Produto do tipo eletrônico pequeno
    ELETRONICO_GRANDE, // Produto do tipo eletrônico grande
    LIVRO // Produto do tipo livro
}
